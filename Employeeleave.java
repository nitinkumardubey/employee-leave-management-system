    public void AutoID()
    {
        Connection();
        try {
            Statement s = con.createStatement();
             ResultSet rs = s.executeQuery("Select MAX(empno) from regestration");
             rs.next();
             rs.getString("MAX(empno)");
             
             if(rs.getString("MAX(empno)") == null)
             {
                 txtno.setText("EMP001");
                 
             }
             else
             {
                 long id = Long.parseLong(rs.getString("MAX(empno)").substring(3,rs.getString("MAX(empno)").length()));
                 id++;
                 
                 txtno.setText("EMP" + String.format("%03d", id));
                 
             }
   
             
        } catch (SQLException ex) {
           ex.printStackTrace();
        }
        
    }
