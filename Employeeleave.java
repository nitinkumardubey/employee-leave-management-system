    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String eno = txtno.getText();
        String ename = txtname.getText();
        String ecategory = txtcategory.getText();
        String esalary = txtsalary.getText();
        
        Connection();
        
        try {
            pst = con.prepareStatement("insert into regestration(empno,empname,empcategory,empsalary)values(?,?,?,?)");
            pst.setString(1, eno);
            pst.setString(2, ename);
            pst.setString(3, ecategory);
            pst.setString(4, esalary);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Employee Added!!!!!!!");
            
            txtname.setText("");
            txtcategory.setText("");
            txtsalary.setText("");
            txtname.requestFocus();
            AutoID();
          
        } catch (SQLException ex) {
           ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

